const canvas = document.querySelector('#canvas');
const ctx = canvas.getContext('2d');
for (let x = 0; x < 20; x++) {
  ctx.lineWidth = 1 + x;
  ctx.beginPath();
  ctx.moveTo(100, 50 + (x * 20));
  ctx.lineTo(500, 50 + (x * 20));
  ctx.stroke();
}
ctx.clearRect(0, 0, 640, 640);
ctx.beginPath();
ctx.lineWidth = 10;
ctx.lineCap = 'butt';
ctx.moveTo(100, 10);
ctx.lineTo(100, 500);
ctx.stroke();
ctx.beginPath();
ctx.lineWidth = 10;
ctx.lineCap = 'round';
ctx.moveTo(200, 10);
ctx.lineTo(200, 500);
ctx.stroke();
ctx.beginPath();
ctx.lineWidth = 10;
ctx.lineCap = 'square';
ctx.moveTo(300, 10);
ctx.lineTo(300, 500);
ctx.stroke();
ctx.clearRect(0, 0, 640, 640);
ctx.beginPath();
ctx.lineWidth = 30;
ctx.lineJoin = 'bevel';
ctx.moveTo(100, 10);
ctx.lineTo(200, 110);
ctx.lineTo(100, 220);
ctx.lineTo(200, 320);
ctx.lineTo(100, 420);
ctx.lineTo(200, 520);
ctx.stroke();